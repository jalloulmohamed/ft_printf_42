#include "ft_printf.h"

void	ft_putadr(unsigned long c)
{
	char	*a;

	a = "0123456789abcdef";
	if (c <= 15)
	{
		ft_putchar(a[c]);
	}
	else
	{
		ft_putadr(c / 16);
		ft_putadr(c % 16);
	}
}
