#include <unistd.h>

int	ft_putstr(char *c)
{
	int	i;

	i = 0;
	if (c != NULL)
	{
		while (c[i])
		{
			write(1, &c[i], 1);
			i++;
		}
	}
	else
	{
		write(1, "(null)", 6);
		return (6);
	}		
	return (i);
}
