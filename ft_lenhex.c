#include "ft_printf.h"

int	ft_lenhex(unsigned int c, char b)
{
	int	i;

	i = 0;
	ft_puthex(c, b);
	if (c == 0)
		i++;
	while (c != 0)
	{
		c = c / 16;
		i++;
	}
	return (i);
}
